mpf_class power(mpf_class b, int e)
{
    mpf_class r(1);
    while (e > 0) { r = r*b; e--; }
    return r;
}

mpf_class fib(int n)
{
    mpf_class a(5, 10000);
    mpf_class sqr5(sqrt(a), 10000);
    mpf_class phi((sqr5+1)/2, 10000);

    return (power(phi, n) - power(1-phi, n))/sqr5;
}


// There is an analytical way to solve this one. If you imagine a rectangular grid measuring a units across and b units down. There are a+1 vertical lines and b+1 horizontal lines. Each rectangle formed on this grid is made up of two vertical lines and two horizontal lines. As there are C(a+1,2)=a(a+1)/2 ways of picking two vertical lines and, similarly, b(b+1)/2, ways of picking two horizontal lines. Hence, there are a(a+1)b(b+1)/4 rectangles on an a by b rectangular grid.
int rectangles(int a, int b)
{
    return a*(a+1)*b*(b+1)/4;
}


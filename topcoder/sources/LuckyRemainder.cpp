int LuckyRemainder :: getLuckyRemainder(string X)
{
    int res = 0;
    int n = (int)(X).size();
    for (int i = 0; i < (int)(X).size(); ++i) {
        int aux = X[i] - '0';
        for (int j = 0; j < n - 1; ++j) {
            aux *= 2;
            aux %= 9;
        }
 // cout << aux << " " << X[i]<<endl;
        res = (res + aux) % 9;
    }
    return res;
}

int ClockWalk :: finalPosition(string flips)
{
  int res=0;
  for (int i=0; i<(int)(flips).size(); ++i)
    if (flips[i]=='h') res=(res+i+1)%12;
    else res=(res+11*(i+1))%12;
  if (!res) res=12;
  return res;
}

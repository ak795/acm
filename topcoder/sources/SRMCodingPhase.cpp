int func(int n)
{
    if (n == 0) return 2;
    if (n == 1) return 4;
    if (n == 2) return 8;
}

int SRMCodingPhase :: countScore(vector <int> points, vector <int> skills, int luck)
{
    int res = 0;
    vector<int> v(3);
    v[1] = 1, v[2] = 2;
    do {
        for (int i = 0; i <= luck; ++i)
            for (int j = 0; j + i<= luck; ++j)
                for (int k = 0; i + j + k <= luck; ++k) {
                    int t0 = max(1, skills[v[0]] - i);
                    int t1 = max(1, skills[v[1]] - j);
                    int t2 = max(1, skills[v[2]] - k);
                    int time = 75;
                    int aux = 0;
                    if (time >= t0) aux += max(0, points[v[0]] - func(v[0]) * t0);
                    time -= t0;
                    if (time >= t1) aux += max(0, points[v[1]] - func(v[1]) * t1);
                    time -= t1;
                    if (time >= t2) aux += max(0, points[v[2]] - func(v[2]) * t2);
                    // cout << i << " " << j << " " << k << " " << endl;
                    // cout << t0 << " " << t1 << " " << t2 << " " << endl;
                    // cout << v[0] << " " << v[1] << " " << v[2] << "** " << endl;
                    // cout << res << endl;
                    res = max(res, aux);
                }
    } while (next_permutation((v).begin(), (v).end()));
    return res;
}

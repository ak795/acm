string LargestSubsequence :: getLargest(string s)
{
    string res;
    for (int i = 0; i < (int)(s).size(); ++i) {
        int tmp = i;
        for (int j = i; j < (int)(s).size(); ++j)
            if (s[j] > s[tmp])
                tmp = j;
        res += s[tmp];
        i = tmp;
    }
    return res;
}

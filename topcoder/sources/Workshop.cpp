int Workshop :: pictureFrames(vector <int> pieces)
{
  if (pieces.size()<3) return 0;
  int res=0;
  for (unsigned i=0; i<(pieces).size(); ++i)
    for (unsigned j=i+1; j<(pieces).size(); ++j)
      for (unsigned k=j+1; k<(pieces).size(); ++k) {
	vector<int> v;
	v.push_back(pieces[i]); v.push_back(pieces[j]); v.push_back(pieces[k]);
	sort((v).begin(), (v).end());
	if (v[0]+v[1]>v[2]) res++;
      }
  return res;
}

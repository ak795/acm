set<VI> s;

void pythagoreanTriangle(int a, int b, int c, int M)
{
    int len = a+b+c;
    if (len >= M) return;

    int m = max(a, b);
    int t = abs(a-b);
    if (c%t == 0) {
	VI v(2);
	v[0] = m;
	v[1] = c;
	s.insert(v);

	for (int i=2; i*len<M; i++) {
	    v[0] += m;
	    v[1] += c;
	    s.insert(v);
	}
    }

    pythagoreanTriangle(a-2*b+2*c, 2*a-b+2*c, 2*a-2*b+3*c, M);
    pythagoreanTriangle(a+2*b+2*c, 2*a+b+2*c, 2*a+2*b+3*c, M);
    pythagoreanTriangle(-a+2*b+2*c, -2*a+b+2*c, -2*a+2*b+3*c, M);
}

